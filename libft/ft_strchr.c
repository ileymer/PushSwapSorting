#include "libft.h"

char	*ft_strchr(const char *str, int ch)
{
	size_t i;

	i = 0;
	while (str[i])
	{
		if (str[i] == (char)ch)
			return ((char *)str + i);
		i++;
	}
	if (ch == '\0')
		return ((char *)str + i);
	return (0);
}
