#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	char	*ptr;

	ptr = s;
	if (s == NULL || f == NULL)
		return ;
	while (*ptr != '\0')
	{
		f(ptr);
		ptr++;
	}
}
